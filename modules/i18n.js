let currentLang = localStorage.getItem('overr_lang') || 'pl';

function getCurrentLang() {
    return currentLang;
}

function setCurrentLang(lang) {
    currentLang = lang;
    localStorage.setItem('overr_lang', lang);
}

function getTranslations(lang = currentLang) {
    return dict[lang];
}

function applyLanguage(lang = currentLang) {
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        if (dict[lang][el.dataset.i18n]) el.innerHTML = dict[lang][el.dataset.i18n];
    });
}

function initLanguageToggle(onChange) {
    const btn = document.getElementById('lang-toggle-btn');
    if (!btn) return;

    btn.innerText = currentLang.toUpperCase();
    btn.addEventListener('click', () => {
        setCurrentLang(currentLang === 'pl' ? 'en' : 'pl');
        btn.innerText = currentLang.toUpperCase();
        onChange?.(currentLang);
    });
}

window.OverrI18n = {
    dict,
    getCurrentLang,
    setCurrentLang,
    getTranslations,
    applyLanguage,
    initLanguageToggle
};
